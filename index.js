// Arrow Functions
const add = (a, b) => a + b;
const subtract = (a, b) => a - b;
const multiply = (a, b) => a * b;
const divide = (a, b) => a / b;

// Increment and Decrement
const increment = n => n + 1;
const decrement = n => n - 1;

// Parsing Functions
const makeInt = n => parseInt(n, 10);
const preserveDecimal = n => parseFloat(n);

// Square Function
const square = x => x * x;